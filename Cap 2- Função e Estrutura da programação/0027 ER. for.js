/**
 *  Estrutura de repetição: for
 *  Ele é semelhante ao while, só que mais amplo
 *  possivelmente é a estrutura de repetição mais fácil de coomprender
 */

for(let i = 0; i < 100; i = i + 3){
    console.log (`A soma de i mais 3 é: ${i}`);
} 
// enquanto 'i' for abaixo de 100, irá somar 3 a ele mesmo.

/**
 *  O que muda do for para o while, é na maioria como se escreve o código, com objetivos iguais basicamente
 *  Há outra coisa que o for serve pra, que é lista de objeto mas é mais avançado
 *  Se for iterar (0 -> 1 -> 2 -> 3 etc.), é recomendado usar for invés de while
 */